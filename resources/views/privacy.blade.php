        <h2>သင့်၏ ကိုယ်ရေးအချက်အလက်များကို အသုံးပြုခြင်း</h2>
        <p>ကုမ္ပဏီသည် အောက်ပါရည်ရွယ်ချက်များအတွက် ကိုယ်ရေးအချက်အလက်များကို အသုံးပြုနိုင်ပါသည်:</p>
        <ul>
            <li><strong>ကျွန်ုပ်တို့၏ ဝန်ဆောင်မှု (ရုပ်ရှင်ကြည့်ရှုခြင်း) ကို ပေးအပ်ရန်နှင့် ထိန်းသိမ်းရန်</strong> (ဝန်ဆောင်မှု၏ အသုံးပြုမှုကို စောင့်ကြည့်ခြင်းအပါအဝင်)။</li>
