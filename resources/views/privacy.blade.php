        <p>သင့်အနေဖြင့် မိုဘိုင်းကိရိယာဖြင့် ဝန်ဆောင်မှုကို ဝင်ရောက်အသုံးပြုသောအခါ ကျွန်ုပ်တို့သည် အောက်ပါအချက်အလက်များကို အလိုအလျောက် စုဆောင်းနိုင်ပါသည် (သို့သော် ထို့နှင့်သာ ကန့်သတ်မထားပါ) - သင်အသုံးပြုသော မိုဘိုင်းကိရိယာအမျိုးအစား၊ သင့်မိုဘိုင်းကိရိယာ၏ ထူးခြားသော ID၊ သင့်မိုဘိုင်းကိရိယာ၏ IP လိပ်စာ၊ သင့်မိုဘိုင်းကိရိယာ၏ မိုဘိုင်းလည်ပတ်ရေးစနစ်၊ သင်အသုံးပြုသော မိုဘိုင်းအင်တာနက် ဘရောက်ဆာအမျိုးအစား၊ ထူးခြားသော ကိရိယာအမှတ်သင်္ကေတများနှင့် အခြားရှာဖွေစစ်ဆေးရေးဆိုင်ရာ အချက်အလက်များ။</p>
        <p>ထို့အပြင် သင်သည် ကျွန်ုပ်တို့၏ ဝန်ဆောင်မှုကို လည်ပတ်သည့်အခါတိုင်း သို့မဟုတ် မိုဘိုင်းကိရိယာဖြင့် ဝန်ဆောင်မှုကို ဝင်ရောက်အသုံးပြုသည့်အခါတိုင်း သင့်ဘရောက်ဆာမှ ပေးပို့သော အချက်အလက်များကိုလည်း ကျွန်ုပ်တို့ စုဆောင်းနိုင်ပါသည်။</p>

