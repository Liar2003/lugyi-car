            <li><strong>စာချုပ်တစ်ခု ထမ်းဆောင်ရန်အတွက်</strong> - သင်ဝယ်ယူထားသော ထုတ်ကုန်များ၊ ပစ္စည်းများ သို့မဟုတ် ဝန်ဆောင်မှုများအတွက် ဝယ်ယူမှုစာချုပ် ဖွံ့ဖြိုးတိုးတက်ရေး၊ လိုက်နာမှုနှင့် ထမ်းဆောင်မှု သို့မဟုတ် ဝန်ဆောင်မှုမှတစ်ဆင့် ကျွန်ုပ်တို့နှင့် ပြုလုပ်ထားသော အခြားမည်သည့်စာချုပ်မဆိုအတွက်။</li>
            <li><strong>သင့်အား ဆက်သွယ်ရန်</strong> - အီးမေးလ်၊ ဖုန်းခေါ်ဆိုမှုများ၊ SMS မက်ဆေ့ချ်များ သို့မဟုတ် အခြားညီမျှသော အီလက်ထရောနစ် ဆက်သွယ်ရေးပုံစံများဖြင့် (ဥပမာ - မိုဘိုင်းအက်ပ်မှ တွန်းကန်အသိပေးချက်များ) ဝန်ဆောင်မှု၏ လုပ်ဆောင်ချက်များ၊ ထုတ်ကုန်များ သို့မဟုတ် စာချုပ်ချုပ်ဆိုထားသော ဝန်ဆောင်မှုများနှင့် သက်ဆိုင်သည့် အပ်ဒိတ်များ သို့မဟုတ် အသိပေးဆက်သွယ်မှုများကို ပေးပို့ရန်။</li>
