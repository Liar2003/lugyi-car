            <li><strong>လုပ်ငန်းခွဲများနှင့်</strong> - ကျွန်ုပ်တို့သည် ကျွန်ုပ်တို့၏ လုပ်ငန်းခွဲများနှင့် သင့်အချက်အလက်များကို မျှဝေနိုင်ပါသည်။ ထိုသို့ပြုလုပ်ပါက ထိုလုပ်ငန်းခွဲများအား ဤကိုယ်ရေးကာကွယ်မှုမူဝါဒကို လေးစားလိုက်နာရန် ကျွန်ုပ်တို့က တောင်းဆိုပါမည်။ လုပ်ငန်းခွဲများတွင် ကျွန်ုပ်တို့၏ မိခင်ကုမ္ပဏီနှင့် အခြားမည်သည့်လက်အောက်ခံကုမ္ပဏီများ၊ ပူးပေါင်းဆောင်ရွက်သည့် မိတ်ဖက်များ သို့မဟုတ် ကျွန်ုပ်တို့က ထိန်းချုပ်ထားသော သို့မဟုတ် ကျွန်ုပ်တို့နှင့်အတူ တူညီသော ထိန်းချုပ်မှုအောက်တွင် ရှိသော အခြားကုမ္ပဏီများ ပါဝင်ပါသည်။</li>
            <li><strong>လုပ်ငန်းမိတ်ဖက်များနှင့်</strong> - ကျွန်ုပ်တို့သည် သင့်အား အချို့သော ထုတ်ကုန်များ၊ ဝန်ဆောင်မှုများ သို့မဟုတ် ပရိုမိုးရှင်းများ ကမ်းလှမ်းရန် ကျွန်ုပ်တို့၏ လုပ်ငန်းမိတ်ဖက်များနှင့် သင့်အချက်အလက်များကို မျှဝေနိုင်ပါသည်။</li>
            <li><strong>အခြားအသုံးပြုသူများနှင့်</strong> - သင်သည် အများသုံးနေရာများတွင် အခြားအသုံးပြုသူများနှင့် ကိုယ်ရေးအချက်အလက်များကို မျှဝေသည့်အခါ သို့မဟုတ် အပြန်အလှန်လုပ်ဆောင်သည့်အခါ ထိုအချက်အလက်များကို အသုံးပြုသူအားလုံးက ကြည့်ရှုနိုင်ပြီး အပြင်ဘက်တွင် အများသိအောင် ဖြန့်ဝေနိုင်ပါသည်။</li>
            <li><strong>သင့်သဘောတူညီချက်ဖြင့်</strong> - အခြားမည်သည့်ရည်ရွယ်ချက်အတွက်မဆို သင့်၏ သဘောတူညီချက်ဖြင့် သင့်၏ ကိုယ်ရေးအချက်အလက်များကို ထုတ်ဖော်နိုင်ပါသည်။</li>
        </ul>

        <hr>

        <h2>သင့်၏ ကိုယ်ရေးအချက်အလက်များကို သိမ်းဆည်းထားခြင်း</h2>
        <p>ကုမ္ပဏီသည် ဤကိုယ်ရေးကာကွယ်မှုမူဝါဒတွင် သတ်မှတ်ထားသော ရည်ရွယ်ချက်များအတွက် လိုအပ်သလောက်သာ သင့်၏ ကိုယ်ရေးအချက်အလက်များကို သိမ်းဆည်းထားပါမည်။ ကျွန်ုပ်တို့သည် ဥပဒေရေးရာ တာဝန်ဝတ္တရားများကို လိုက်နာရန် (ဥပမာ - သက်ဆိုင်ရာဥပဒေများကို လိုက်နာရန် သင့်အချက်အလက်များကို သိမ်းဆည်းထားရန် လိုအပ်ပါက)၊ အငြင်းပွားမှုများကို ဖြေရှင်းရန်နှင့် ကျွန်ုပ်တို့၏ ဥပဒေရေးရာ သဘောတူညီချက်များနှင့် မူဝါဒများကို အတည်ပြုရန် လိုအပ်သည့်အတိုင်းအတာအထိ သင့်၏ ကိုယ်ရေးအချက်အလက်များကို သိမ်းဆည်းပြီး အသုံးပြုပါမည်။</p>

        <p>ကုမ္ပဏီသည် အတွင်းပိုင်း ဆန်းစစ်လေ့လာမှုဆိုင်ရာ ရည်ရွယ်ချက်များအတွက် အသုံးပြုမှုအချက်အလက်များကိုလည်း သိမ်းဆည်းထားပါမည်။ အသုံးပြုမှုအချက်အလက်များကို ယေဘုယျအားဖြင့် ပိုမိုတိုတောင်းသော ကာလအတွက် သိမ်းဆည်းထားပြီး ကျွန်ုပ်တို့၏ ဝန်ဆောင်မှု၏ လုံခြုံရေးကို ခိုင်မာစေရန် သို့မဟုတ် လုပ်ဆောင်ချက်ကို မြှင့်တင်ရန် ဤအချက်အလက်ကို အသုံးပြုသည့်အခါ သို့မဟုတ် ဥပဒေအရ ဤအချက်အလက်ကို ပိုမိုကြာရှည်စွာ သိမ်းဆည်းထားရန် တာဝန်ရှိသည့်အခါများတွင် ခြွင်းချက်ဖြစ်ပါသည်။</p>

        <h2>သင့်၏ ကိုယ်ရေးအချက်အလက်များ လွှဲပြောင်းခြင်း</h2>
        <p>သင့်၏ အချက်အလက်များ (ကိုယ်ရေးအချက်အလက်များ အပါအဝင်) ကို ကုမ္ပဏီ၏ လုပ်ငန်းလည်ပတ်ရာ ရုံးများနှင့် အချက်အလက်များကို ပြုလုပ်ပေးသူများ တည်ရှိရာ အခြားနေရာများတွင် ပြုလုပ်ပါသည်။ ဆိုလိုသည်မှာ ဤအချက်အလက်များကို သင့်နိုင်ငံ၊ ပြည်နယ်၊ တိုင်းဒေသကြီး သို့မဟုတ် အခြားအစိုးရ အာဏာပိုင်နယ်နိမိတ်ပြင်ပ ကွန်ပျူတာများသို့ လွှဲပြောင်းပြီး သိမ်းဆည်းနိုင်ပါသည်။ ထိုနေရာများတွင် အချက်အလက် ကာကွယ်ရေး ဥပဒေများသည် သင့်နယ်နိမိတ်အတွင်းရှိ ဥပဒေများနှင့် ကွဲပြားနိုင်ပါသည်။</p>

        <p>ဤကိုယ်ရေးကာကွယ်မှုမူဝါဒကို သဘောတူညီပြီး ထိုသို့သော အချက်အလက်များကို တင်ပြခြင်းသည် ထိုလွှဲပြောင်းမှုကို သဘောတူညီကြောင်း ကိုယ်စားပြုပါသည်။</p>

        <p>ကုမ္ပဏီသည် သင့်အချက်အလက်များကို လုံခြုံစွာဆက်ဆံခြင်းနှင့် ဤကိုယ်ရေးကာကွယ်မှုမူဝါဒနှင့်အညီ ဆောင်ရွက်ရန် လိုအပ်သမျှ အားလုံးသော အဆင့်များကို ဆောင်ရွက်ပါမည်။ သင့်အချက်အလက်များနှင့် အခြားကိုယ်ရေးအချက်အလက်များ၏ လုံခြုံရေးအပါအဝင် လုံလောက်သော ထိန်းချုပ်မှုများ မရှိပါက သင့်၏ ကိုယ်ရေးအချက်အလက်များ လွှဲပြောင်းခြင်းကို အဖွဲ့အစည်း တစ်ခု သို့မဟုတ် နိုင်ငံတစ်ခုသို့ လုပ်ဆောင်မည် မဟုတ်ပါ။</p>

        <h2>သင့်၏ ကိုယ်ရေးအချက်အလက်များကို ဖျက်ပစ်ခြင်း</h2>
        <p>ကျွန်ုပ်တို့ စုဆောင်းထားသော သင့်နှင့်ပတ်သက်သည့် ကိုယ်ရေးအချက်အလက်များကို ဖျက်ပစ်ရန် သို့မဟုတ် ဖျက်ပစ်ရာတွင် ကျွန်ုပ်တို့က ကူညီရန် တောင်းဆိုရန် သင့်တွင် အခွင့်အရေးရှိပါသည်။</p>

        <p>ကျွန်ုပ်တို့၏ ဝန်ဆောင်မှုသည် ဝန်ဆောင်မှုအတွင်းမှ တစ်ဆင့် သင့်နှင့်ပတ်သက်သည့် အချက်အလက်အချို့ကို ဖျက်ပစ်နိုင်စွမ်း ပေးနိုင်ပါသည်။</p>

        <p>သင့်တွင် အကောင့်ရှိပါက အကောင့်အတွင်းသို့ ဝင်ရောက်ပြီး သင့်၏ ကိုယ်ရေးအချက်အလက်များကို စီမံခန့်ခွဲနိုင်သည့် အကောင့်ဆက်တင်များသို့ သွားခြင်းဖြင့် သင့်အချက်အလက်များကို မည်သည့်အချိန်တွင်မဆို အပ်ဒိတ်လုပ်နိုင်၊ ပြင်ဆင်နိုင် သို့မဟုတ် ဖျက်ပစ်နိုင်ပါသည်။ သင်သည် ကျွန်ုပ်တို့ထံသို့ ပေးအပ်ထားသော မည်သည့်ကိုယ်ရေးအချက်အလက်ကိုမဆို အသုံးပြုခွင့်ရရန်၊ ပြင်ဆင်ရန် သို့မဟုတ် ဖျက်ပစ်ရန် တောင်းဆိုရန် ကျွန်ုပ်တို့ထံ ဆက်သွယ်နိုင်ပါသည်။</p>

        <p>သို့သော် ကျွန်ုပ်တို့တွင် ဥပဒေရေးရာ တာဝန်ဝတ္တရား သို့မဟုတ် တရားဝင်သော အခြေခံအကြောင်းပြချက်တစ်ခုခု ရှိသည့်အခါ ကျွန်ုပ်တို့သည် အချို့သောအချက်အလက်များကို သိမ်းဆည်းထားရန် လိုအပ်နိုင်သည်ကို သတိပြုပါ။</p>

        <hr>

        <h2>သင့်၏ ကိုယ်ရေးအချက်အလက်များ ထုတ်ဖော်ခြင်း</h2>

        <h3>လုပ်ငန်းဆိုင်ရာ လွှဲပြောင်းမှုများ</h3>
        <p>ကုမ္ပဏီသည် ပေါင်းစည်းမှု၊ ဝယ်ယူမှု သို့မဟုတ် ပိုင်ဆိုင်မှုရောင်းချမှုတွင် ပါဝင်ပါက သင့်၏ ကိုယ်ရေးအချက်အလက်များ လွှဲပြောင်းခံရနိုင်ပါသည်။ သင့်၏ ကိုယ်ရေးအချက်အလက်များ လွှဲပြောင်းခံရပြီး ကွဲပြားသော ကိုယ်ရေးကာကွယ်မှုမူဝါဒတစ်ခု၏ လက်အောက်သို့ ရောက်ရှိမည့် အချိန်မတိုင်မီ ကျွန်ုပ်တို့က အသိပေးပါမည်။</p>

        <h3>တရားဥပဒေစိုးမိုးရေး</h3>
        <p>အချို့သော အခြေအနေများအောက်တွင် အများပြည်သူဆိုင်ရာ အာဏာပိုင်များ (ဥပမာ - တရားရုံး သို့မဟုတ် အစိုးရအေဂျင်စီ) ၏ တရားဝင်တောင်းဆိုမှုကို တုန့်ပြန်ရာတွင် ဥပဒေအရ လိုအပ်ပါက ကုမ္ပဏီသည် သင့်၏ ကိုယ်ရေးအချက်အလက်များကို ထုတ်ဖော်ရန် တာဝန်ရှိနိုင်ပါသည်။</p>

        <h3>အခြားသော ဥပဒေရေးရာ လိုအပ်ချက်များ</h3>
        <p>ကုမ္ပဏီသည် အောက်ပါတို့ကို လုပ်ဆောင်ရန် လိုအပ်သည်ဟု ရိုးသားစွာ ယုံကြည်လျှင် သင့်၏ ကိုယ်ရေးအချက်အလက်များကို ထုတ်ဖော်နိုင်ပါသည်:</p>
        <ul>
            <li>ဥပဒေရေးရာ တာဝန်ဝတ္တရားတစ်ရပ်ကို လိုက်နာရန်</li>
